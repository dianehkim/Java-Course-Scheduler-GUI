    private void scheduleCourseSubmitButtonActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_scheduleCourseSubmitButtonActionPerformed
        // TODO add your handling code here:
        student_ID = (String) selectStudentComboBox.getSelectedItem();
        course_code = (String) ChangeCourseComboBox.getSelectedItem();
        int seats = CourseQueries.getCourseSeats(currentSemester, course_code);
        int count = ScheduleQueries.getScheduledStudentCount(currentSemester, course_code);
        Timestamp timestamp = new Timestamp(System.currentTimeMillis());
        String status;
        String statusPrint;
        
        if (seats > count) 
        {
            status = "s";
            statusPrint = "scheduled";
        }
        else
        {
            status = "w";
            statusPrint = "waitlisted";
        }
    
        ScheduleEntry entry = new ScheduleEntry(currentSemester, student_ID, course_code, status, timestamp);  
        ScheduleQueries.addScheduleEntry(entry);
        scheduleCoursesStatusLabel.setText("Student " + selectStudentComboBox.getSelectedItem() + " was " + statusPrint + " for " + course_code);

        rebuildSemesterComboBoxes();
    }//GEN-LAST:event_scheduleCourseSubmitButtonActionPerformed

