    private void displayScheduleButtonActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_displayScheduleButtonActionPerformed
        // TODO add your handling code here:
        DefaultTableModel scheduleTable = new DefaultTableModel();
        scheduleTable.addColumn("Course Code");
        scheduleTable.addColumn("Status");
        String courseCode = addCourseCodeTextField.getText();
        int seats = (int)addCourseSeatsSpinner.getValue();
        student_ID = (String) DisplayScheduleStudentComboBox.getSelectedItem();
        
        
        ArrayList<String> courseCodes = ScheduleQueries.getCourseCode(currentSemester, student_ID);
        for(int i=0; i<courseCodes.size(); i++)
        {
            String status = ScheduleQueries.getStatus(currentSemester, student_ID, courseCodes.get(i));
            
            if ("w".equals(status))
            {
                status = "waitlisted";
            }
            else
            {
                status = "scheduled";
            }
            
            scheduleTable.addRow(new Object[] {courseCodes.get(i), status});
        }
        displayScheduleTable.setModel(scheduleTable);
        rebuildCourseComboBoxes();
        rebuildSemesterComboBoxes();
        
    }//GEN-LAST:event_displayScheduleButtonActionPerformed

